											<li><b>Le maltage</b></br>
												Le principe du maltage consiste à produire les enzymes nécessaires à la saccharification de l'amidon, et donc, à la fabrication de l'alcool lors de la fermentation.
											Ceci se passe en quatre étapes : le trempage, la germination, le touraillage (séchage : c'est la température atteinte en fin de touraillage qui va déterminer la couleur du malt.) et le dégermage.</br>
											Ingrédients : orge et eau</li>
											<li><b>Le brassage</b></br>
												Cette étape, appelée aussi saccharification, consiste à transformer les sucres complexes (amidon) contenus dans le grain en sucres simples fermentescibles (glucose, maltose et dextrose) et non fermentescibles (dextrines), grâce à l'action des enzymes du malt, activées par chauffage du malt dans de l'eau.
			On filtre et rince à l'eau chaude ce mélange (maische) pour obtenir le moût.</br>
											Ingrédients : eau et malt</li>
											<li><b>L'ébullition - le houblonnage</b></br>
												Cette étape a pour objectif la destruction des enzymes, la stérilisation et la stabilisation du moût.
			C'est à ce moment que le houblon est ajouté : le houblon possède des vertus conservatrices, donne de l'amertume et produit des arômes.</br>
											Ingrédients : malt et houblon</li>
